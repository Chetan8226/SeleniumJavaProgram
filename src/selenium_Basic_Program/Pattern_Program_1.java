package selenium_Basic_Program;

public class Pattern_Program_1 
{

	public static void main(String[] args) 
	{
		for(int row=1;row<=2;row++) // Rows
		{
			for(int star=1;star<=3;star++)// Columns
			{
				System.out.print("*");
			}
			System.out.println();
		}
		

	}

}
